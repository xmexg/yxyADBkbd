	public String get_logo(){
		//获取随机logo
		Random rand = new Random();
		return logo[rand.nextInt(logo.length)];
	}
	
	public String get_logo(int num,boolean random){
		/**数字，是否随机。
		 * 如果随机:
		 * 		如果数字范围有效:在给定范围内随机
		 * 		如果数字范围无效:在有效范围内随机
		 * 如果不随机:
		 * 		如果数字范围有效:返回指定位置logo
		 * 		如果数字范围无效:返回第一个logo
		 */
		if((num>logo.length)||(num<0)) {
			num = logo.length;
		}
		if(random) {
			Random rand = new Random();
			return logo[rand.nextInt(num)];
		}else {
			return logo[0];
		}
	}
